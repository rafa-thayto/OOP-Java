import java.util.ArrayList;
public class ArrayLists {
	public static void main(String[] args) {
		
		// declara um ArrayList de String
		ArrayList<String> cores = new ArrayList<String>();
		
		// adiciona novos elementos ao arraylist
		cores.add("Branco"); 
		cores.add(0, "Vermelho");
		cores.add("Amarelo");
		cores.add("Azul");
		
		// exibe todos os elementos do arraylist
		System.out.println(cores.toString());
		
		// quantos elementos existem no arraylist
		System.out.println(cores.size());
		
		// pega um elemento do arraylist
		System.out.println(cores.get(2));
		
		// remove um elemento do arraylist
		cores.remove("Branco");
		
		// mostra todos os elementos do arraylist
		System.out.println(cores.toString());
		
		// pesquisa um elemento no arraylist
		System.out.println(cores.contains("Amarelo"));
		System.out.println(cores.contains("Branco"));
		// pesquisa se um elemento não está no arraylist
		System.out.println(!(cores.contains("Branco")));
	}
}